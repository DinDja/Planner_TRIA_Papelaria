export interface Proto {
  ch: string
  feat: GlyphFeat
}

let cache: Proto[] | null = null

/** Protótipos featurizados (cache de módulo — computado uma única vez). */
export function getPrototypes(): Proto[] {
  if (!cache) {
    cache = DEFS.map((d) => ({
      ch: d.ch,
      feat: featurizePrototype(
        d.s.map((st) => st.map(([x, y]): Pt => ({ x, y }))),
        d.z,
        d.rel,
        { dotAbove: d.dA, dotBelow: d.dB, barAcross: d.bar },
      ),
    }))
  }
  return cache
}

/** Traços brutos (caixa unitária) do primeiro alógrafo — testes e aprendizado. */
export function getPrototypeStrokes(ch: string): Pt[][] | null {
  const def = DEFS.find((d) => d.ch === ch)
  if (!def) return null
  return def.s.map((st) => st.map(([x, y]) => ({ x, y })))
}
